function Untitled2
x0 = 3.0; %точки полученные графически
y0 = 1.0; %возьмем их за начальное приближение
x = cos(y0) + 3;  
y = 0.5 - cos(x0 - 1);

e = 0.01; %точность
k = 0; %счетчик итераций

while or((abs(x - x0 ) > e), (abs(y - y0) > e)) %пока точность не будет достигнута
x0 = x;
y0 = y;
x = cos(y0) + 3;
y = 0.5 - cos(x0 - 1);
k = k + 1;
end;
disp(k);
disp(x);
disp(y);